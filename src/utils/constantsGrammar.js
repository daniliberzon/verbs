export const findGizra = (tense, binyan, root) => {
    if (root.length > 9)
        return FOUR_LETTERS;

    if (root[root.length - 1] === "ה" && (binyan === "PA'AL" || binyan === "PI'EL" || binyan === "HITPA'EL"))
        return LAMED_HEI;

    if (GRONIYOTS.find((el) => el === root[0]))
        if (menuStructure[tense][binyan].find((el) => el === PEI_GRONIYOT))
            return PEI_GRONIYOT;

    if (root[0] === "נ")
        if (menuStructure[tense][binyan].find((el) => el === PEI_NUN))
            return PEI_NUN;
    
    if (IUD_HEI_NUN.find((el) => el === root[0]) && (tense === "Imperative") && (binyan === "PA'AL"))
        return PEI_IUD_HEI_NUN;
        
    if (root[0] === "י")
        if (menuStructure[tense][binyan].find((el) => el === PEI_IUD))
            return PEI_IUD;

    if (GRONIYOTS.find((el) => el === root[4])) {
        if (tense === "Future" && binyan === "PA'AL")
            return SHLEMIM_EFAL;
        else if (menuStructure[tense][binyan].find((el) => el === AYN_GRONIYOT))
            return AYN_GRONIYOT;
    }

    if (root[4] === "י" && binyan === "HIF'IL")
        return AYN_IUD;
    
    if (root[4] === "י" || root[4] === "ו")
        if (menuStructure[tense][binyan].find((el) => el === AYN_VAV_IUD))
            return AYN_VAV_IUD;
    
    if (root[4] === "ר" && tense === "Past" && binyan === "PI'EL")
        return AYN_REISH;

    if (GRONIYOTS.find((el) => el === root[root.length-1]) && (tense === "Future") && (binyan === "PA'AL"))
        return SHLEMIM_EFAL;
    
    if ((tense === "Future") && (binyan === "PA'AL") && (EFAL_EXCEPTIONS.find((el) => el === root)))
        return SHLEMIM_EFAL;
    
    if ((root[root.length-1] === "ח" || root[root.length-1] === "ע") && tense === "Present" && ((binyan === "PA'AL") || (binyan === "NIF'AL")))
        return LAMED_HET_AYN;
        
    if (GRONIYOTS.find((el) => el === root[root.length-1]))
        if (menuStructure[tense][binyan].find((el) => el === LAMED_GRONIYOT))
            return LAMED_GRONIYOT;

    if (tense === "Future" && binyan === "PA'AL")
        return SHLEMIM_EFOL;

    return SHLEMIM;
}